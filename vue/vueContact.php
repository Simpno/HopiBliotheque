    <h1>Politique de protection des données personnelles</h1>
    <p>Dernière mise à jour : 22 septembre 2025</p>

    <h2>1. Données personnelles collectées</h2>
    <p>Nous collectons uniquement les données nécessaires au bon fonctionnement de l'application :</p>
    <ul>
        <li>Nom et prénom</li>
        <li>Adresse email</li>
        <li>Identifiants de connexion (login, mot de passe hashé avec l'algorythme ...)</li>
        <li>Historique des emprunts de livres</li>
    </ul>

    <h2>2. Finalité des collectes</h2>
    <p>Ces données sont utilisées pour :</p>
    <ul>
        <li>Permettre la gestion de votre compte utilisateur</li>
        <li>Assurer le suivi des emprunts et retours de livres</li>
        <li>Améliorer les services proposés</li>
    </ul>

    <h2>3. Base légale du traitement</h2>
    <p>Le traitement de vos données repose sur :</p>
    <ul>
        <li>Votre consentement (formulaire de contact, inscription newsletter)</li>
        <li>L'exécution d'un contrat (gestion des emprunts et accès au service)</li>
        <li>Le respect d'obligations légales (conservation minimale)</li>
    </ul>

    <h2>4. Durée de conservation</h2>
    <ul>
        <li>Données de compte : conservées tant que le compte est actif</li>
        <li>Données liées aux emprunts : 3 ans après la fin de la relation</li>
        <li>Données de contact : 3 ans maximum</li>
    </ul>

    <h2>5. Partage des données</h2>
    <p>Vos données ne sont <strong>jamais revendues</strong>. Elles peuvent être accessibles uniquement à :</p>
    <ul>
        <li>L'équipe de gestion de la bibliothèque</li>
        <li>Nos prestataires techniques (hébergement sécurisé, maintenance)</li>
        <li>Les autorités compétentes si la loi l'exige</li>
    </ul>

    <h2>6. Vos droits</h2>
    <p>Conformément au RGPD, vous disposez des droits suivants :</p>
    <ul>
        <li>Droit d'accès à vos données</li>
        <li>Droit de rectification</li>
        <li>Droit à l'effacement</li>
        <li>Droit à la portabilité</li>
        <li>Droit d'opposition</li>
    </ul>
    <p>Pour exercer ces droits, vous pouvez nous contacter à l'adresse mail indiquée ci-dessous.</p>

